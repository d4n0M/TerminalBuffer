package org.example.model;

/**
 * Represents standard terminal colors.
 */
public enum Color {
    /** The default terminal color. */
    DEFAULT,
    /** Standard black. */
    BLACK,
    /** Standard red. */
    RED,
    /** Standard green. */
    GREEN,
    /** Standard yellow. */
    YELLOW,
    /** Standard blue. */
    BLUE,
    /** Standard magenta. */
    MAGENTA,
    /** Standard cyan. */
    CYAN,
    /** Standard white. */
    WHITE,
    /** Bright version of black (often dark gray). */
    BRIGHT_BLACK,
    /** Bright red. */
    BRIGHT_RED,
    /** Bright green. */
    BRIGHT_GREEN,
    /** Bright yellow. */
    BRIGHT_YELLOW,
    /** Bright blue. */
    BRIGHT_BLUE,
    /** Bright magenta. */
    BRIGHT_MAGENTA,
    /** Bright cyan. */
    BRIGHT_CYAN,
    /** Bright white. */
    BRIGHT_WHITE
}